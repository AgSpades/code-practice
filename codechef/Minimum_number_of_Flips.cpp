inline void solve()
{
    int n;
    cin >> n;
    int arr[n];
    for (int i = 0; i < n; i++)
        cin >> arr[i];
    if (n & 1)
        cout << -1 << '\n';
    else
    {
        int np = 0, nn = 0;
        for (int num : arr)
            num > 0 ? np++ : nn++;
        np > nn ? cout << n / 2 - nn << '\n' : cout << n / 2 - np << '\n';
    }
}

