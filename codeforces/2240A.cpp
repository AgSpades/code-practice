inline void solve()
{
    ll n, k;
    cin >> n >> k;
    ll tot = 0, curr = 1;
    while (n > 0 && curr <= n)
    {
        ll left = n / curr;
        ll take = min(k, left);
        tot += take;
        n -= take * curr;
        if (curr > n / 2)
            break;

        curr *= 2;
    }
    cout << tot << '\n';
}

